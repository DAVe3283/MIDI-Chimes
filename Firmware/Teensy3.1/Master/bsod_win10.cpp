void draw_BSOD(ILI9341_t3& display)
{
    // Set colors
    display.fillScreen(bsod_bg_windows_10);
    display.setTextColor(bsod_fg_windows_10);

    // Decompress :( emoticon
    int offset(0);
    for (size_t i(0); i < (sizeof(bsod_frown) / sizeof(bsod_frown[0])); i += 2)
    {
        const uint16_t run_length(bsod_frown[i]);
        const uint16_t color(bsod_frown[i + 1]);
        for (int r(0); r < run_length; ++r)
        {
            const int x(offset % bsod_frown_width);
            const int y(offset / bsod_frown_width);
            offset++;

            // Top half
            display.drawPixel(20 + x, 20 + y, color);

            // Bottom half (mirrored)
            display.drawPixel(20 + x, 20 + (bsod_frown_height - 1) - y, color);
        }
    }

    // Decompress useless generic Windows 10 BSOD text
    offset = 0;
    for (size_t i(0); i < (sizeof(bsod_text) / sizeof(bsod_text[0])); i += 2)
    {
        const uint16_t run_length(bsod_text[i]);
        const uint16_t color(bsod_text[i + 1]);
        for (int r(0); r < run_length; ++r)
        {
            const int x(offset % bsod_text_width);
            const int y(offset / bsod_text_width);
            display.drawPixel(78 + x, 30 + y, color);
            offset++;
        }
    }

    // Prepare to print error text
    display.setCursor(0, 124);
    display.setFontAdafruit();
}
